def get_btn_radius(style: str) -> str:
    """Convert button style name to CSS border-radius."""
    mapping = {
        "rounded": "12px",
        "pill": "50px",
        "square": "0px",
    }
    return mapping.get(style, "12px")


def validate_hex_color(value: str) -> bool:
    """Check if a string is a valid hex color."""
    return bool(re.match(r'^#([0-9a-fA-F]{3}|[0-9a-fA-F]{6})$', value.strip()))


def parse_style_input(text: str) -> dict:
    """
    Parse a single message with style fields:
    Background: #000000
    Text: #ffffff
    Button: #FF3B3B
    Button Text: #ffffff
    Style: rounded
    """
    style = {}
    field_map = {
        "background": "bg_color",
        "bg": "bg_color",
        "text": "text_color",
        "text color": "text_color",
        "accent": "accent_color",
        "button": "button_color",
        "button color": "button_color",
        "btn": "button_color",
        "button text": "button_text_color",
        "btn text": "button_text_color",
        "style": "button_style",
        "button style": "button_style",
        "shape": "button_style",
    }

    for line in text.strip().split("\n"):
        line = line.strip()
        if ":" not in line:
            continue
        key, val = line.split(":", 1)
        key = key.strip().lower()
        val = val.strip()

        mapped = field_map.get(key)
        if mapped:
            if mapped == "button_style":
                if val.lower() in ("rounded", "pill", "square"):
                    style[mapped] = val.lower()
            elif validate_hex_color(val):
                style[mapped] = val
            # Also set accent_color when button_color is set
            if mapped == "button_color" and "accent_color" not in style:
                style["accent_color"] = val

    return style


def merge_style(base: dict, override: dict) -> dict:
    """Merge override style on top of base style."""
    result = base.copy()
    result.update({k: v for k, v in override.items() if v})
    return result
